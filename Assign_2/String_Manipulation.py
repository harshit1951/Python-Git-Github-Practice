str = "hello, World! Welcome to the Python string manipulation demo."

# Converts first letter to uppercase
print(str.capitalize()) 
# Output: Hello, world! welcome to the python string manipulation demo.

# Converts to lowercase
print(str.lower()) 
# Output: hello, world! welcome to the python string manipulation demo.

# Counts the word
x = str.count("l")
print(x) 
# Output: 5

y = str.find("Python")
print(y) 
# Output: 29